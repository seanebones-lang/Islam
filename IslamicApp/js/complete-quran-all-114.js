// Function to verify complete Quran is loaded
window.verifyCompleteQuranAll114 = function() {
    const loadedSurahs = Object.keys(window.completeQuranAll114).map(Number).sort((a, b) => a - b);
    return loadedSurahs.length === 114 && loadedSurahs.every((num, index) => num === index + 1);
};

// Function to get surah data
window.getSurahDataAll114 = function(surahNumber) {
    return window.completeQuranAll114[surahNumber] || null;
};

// Function to get all surahs metadata
window.getAllSurahsAll114 = function() {
    const surahs = {};
    for (let i = 1; i <= 114; i++) {
        if (window.completeQuranAll114[i]) {
            surahs[i] = {
                name: window.completeQuranAll114[i].name,
                english: window.completeQuranAll114[i].english,
                arabic: window.completeQuranAll114[i].arabic,
                type: window.completeQuranAll114[i].type,
                verses: window.completeQuranAll114[i].verses,
                juz: window.completeQuranAll114[i].juz
            };
        }
    }
    return surahs;
};

// Function to search across all surahs
window.searchQuranAll114 = function(query) {
    const results = [];
    const searchTerm = query.toLowerCase();
    
    Object.keys(window.completeQuranAll114).forEach(surahNum => {
        const surah = window.completeQuranAll114[surahNum];
        
        // Search in surah names
        if (surah.name.toLowerCase().includes(searchTerm) || 
            surah.english.toLowerCase().includes(searchTerm) ||
            surah.arabic.includes(query)) {
            results.push({
                type: 'surah',
                surah: parseInt(surahNum),
                name: surah.name,
                english: surah.english,
                arabic: surah.arabic,
                match: 'title'
            });
        }
    });
    
    return results;
};

