// Function to verify complete Quran is loaded
window.verifyCompleteQuran = function() {
    const loadedSurahs = Object.keys(window.completeQuranFull).map(Number).sort((a, b) => a - b);
    return loadedSurahs.length === 114 && loadedSurahs.every((num, index) => num === index + 1);
};

// Function to get surah data
window.getSurahData = function(surahNumber) {
    return window.completeQuranFull[surahNumber] || null;
};

// Function to get all surahs metadata
window.getAllSurahs = function() {
    const surahs = {};
    for (let i = 1; i <= 114; i++) {
        if (window.completeQuranFull[i]) {
            surahs[i] = {
                name: window.completeQuranFull[i].name,
                english: window.completeQuranFull[i].english,
                arabic: window.completeQuranFull[i].arabic,
                type: window.completeQuranFull[i].type,
                verses: window.completeQuranFull[i].verses,
                juz: window.completeQuranFull[i].juz
            };
        }
    }
    return surahs;
};

// Function to search across all surahs
window.searchQuran = function(query) {
    const results = [];
    const searchTerm = query.toLowerCase();
    
    Object.keys(window.completeQuranFull).forEach(surahNum => {
        const surah = window.completeQuranFull[surahNum];
        
        // Search in surah names
        if (surah.name.toLowerCase().includes(searchTerm) || 
            surah.english.toLowerCase().includes(searchTerm) ||
            surah.arabic.includes(query)) {
            results.push({
                type: 'surah',
                surah: parseInt(surahNum),
                name: surah.name,
                english: surah.english,
                arabic: surah.arabic,
                match: 'title'
            });
        }
    });
    
    return results;
};

